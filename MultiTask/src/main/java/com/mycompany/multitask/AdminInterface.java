    private void txtSalarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSalarioKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        // Permitir solo n√∫meros y la tecla de retroceso
        if (!Character.isDigit(c) && c != KeyEvent.VK_BACK_SPACE) {
            evt.consume(); // Evita que se escriban caracteres no v√°lidos

        }
    }//GEN-LAST:event_txtSalarioKeyTyped

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblEmpleados.getSelectedRow();
        if (selectedRow >= 0) {
            // Confirmar la eliminaci√≥n
            int confirm = JOptionPane.showConfirmDialog(this, "¬øEst√°s seguro de que deseas eliminar este empleado?",
                    "Confirmar Eliminaci√≥n", JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (confirm == JOptionPane.YES_OPTION) {
                // Si el usuario confirma, proceder a eliminar
                int id = Integer.parseInt(tblEmpleados.getValueAt(selectedRow, 0).toString());
                if (empleadosDAO.eliminarEmpleado(id)) {
                    JOptionPane.showMessageDialog(this, "Empleado eliminado exitosamente");
                    actualizarTabla(); // Llamar para actualizar la tabla
                } else {
                    JOptionPane.showMessageDialog(this, "Error al eliminar empleado");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor selecciona un empleado para eliminar");
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

