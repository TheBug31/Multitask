    // M√©todo para manejar el evento de agregar un empleado
    private void agregarEmpleadoDirecto() {
        // Recoger datos de los campos de texto
        String nombre = txtNombre.getText().trim();
        String cargo = txtCargo.getText().trim();
        String salarioText = txtSalario.getText().trim();

        // Validar que los campos no est√©n vac√≠os
        if (nombre.isEmpty() || cargo.isEmpty() || salarioText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos deben ser llenados.", "Error de Validaci√≥n", JOptionPane.ERROR_MESSAGE);
            return; // Salir del m√©todo si hay campos vac√≠os
        }

        try {
            double salario = Double.parseDouble(salarioText);

            // Llamada al DAO para agregar empleado
            boolean exito = empleadosDAO.agregarEmpleado(nombre, cargo, salario);

            // Mostrar mensaje de √©xito o error
            String mensaje = exito
                    ? "Empleado agregado correctamente."
                    : "El empleado " + nombre + " ya existe en la base de datos."; // Mensaje de empleado duplicado
            JOptionPane.showMessageDialog(this, mensaje, "Resultado",
                    exito ? JOptionPane.INFORMATION_MESSAGE : JOptionPane.ERROR_MESSAGE);

            // Limpiar los campos despu√©s de agregar
            txtNombre.setText("");
            txtCargo.setText("");
            txtSalario.setText("");
            actualizarTabla();
        } catch (NumberFormatException ex) {
            // Manejo de error si el salario no es un n√∫mero v√°lido
            JOptionPane.showMessageDialog(this, "Ingrese un salario v√°lido.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * M√©todo para cargar empleados y actualizar la tabla.
     */
    private void actualizarTabla() {
        List<String[]> empleados = empleadosDAO.obtenerEmpleados();
        // Clear current table data and set new data
        DefaultTableModel model = (DefaultTableModel) tblEmpleados.getModel();
        model.setRowCount(0); // Clear the table
        for (String[] empleado : empleados) {
            model.addRow(empleado);
        }
    }

